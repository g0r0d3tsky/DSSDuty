CREATE TABLE "USER" (
  "id" UUID PRIMARY KEY,
  "username" VARCHAR(255) NOT NULL,
  "duty_amount" int,
  "role" VARCHAR(255) NOT NULL
);

CREATE TABLE "DUTY" (
  "id" UUID PRIMARY KEY,
  "date" TIMESTAMP NOT NULL,
  "user_id1" UUID,
  "user_id2" UUID
);

CREATE TABLE "RewardsSanctions" (
  "id" UUID PRIMARY KEY,
  "user_id" UUID NOT NULL,
  "rewards" int,
  "sanctions" int,
  "info" string,
  "timestamp" TIMESTAMP
);

CREATE TABLE "EVENT" (
  "id" UUID PRIMARY KEY,
  "user_id" UUID NOT NULL,
  "type" VARCHAR(255) NOT NULL,
  "timestamp" TIMESTAMP NOT NULL
);

